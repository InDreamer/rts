def render_meta_chip(label: str, value: Any) -> str:
    if value in (None, "", []):
        return ""
    if isinstance(value, list):
        value = ", ".join(value)
    return f'<span class="chip subtle">{esc(label)}: {esc(str(value))}</span>'


def render_meta_panel(doc: Doc, docs_by_rel: dict[str, Doc]) -> str:
    read_when = render_meta_list("适合阅读", doc.meta.get("read_when"), docs_by_rel, doc.output_path)
    skip_when = render_meta_list("可跳过", doc.meta.get("skip_when"), docs_by_rel, doc.output_path)
    sources = render_meta_list("权威来源 / 关联来源", doc.meta.get("source_of_truth"), docs_by_rel, doc.output_path)
    if not any([read_when, skip_when, sources]):
        return ""
    return f"""<section class="sidebar-card meta-card">
      <p class="eyebrow">阅读提示</p>
      {read_when}
      {skip_when}
      {sources}
    </section>"""


def render_meta_list(
    title: str,
    values: Any,
    docs_by_rel: dict[str, Doc],
    current_output: Path,
) -> str:
    if not values:
        return ""
    if isinstance(values, str):
        values = [values]
    items = "\n".join(f"<li>{render_meta_value(v, docs_by_rel, current_output)}</li>" for v in values)
    return f"<h3>{esc(title)}</h3><ul>{items}</ul>"


def render_meta_value(value: str, docs_by_rel: dict[str, Doc], current_output: Path) -> str:
    clean = value.strip().strip("`")
    target = docs_by_rel.get(clean)
    if target:
        href = rel_url(current_output, target.output_path)
        return f'<a href="{href}">{esc(clean)}</a>'
    return esc(value)


def render_side_nav(doc: Doc, docs: list[Doc]) -> str:
    peers = [item for item in docs if item.category == doc.category]
    if len(peers) <= 1:
        return '<p class="empty-note">暂无同组文档。</p>'
    links = []
    for peer in peers[:14]:
        href = rel_url(doc.output_path, peer.output_path)
        active = " active" if peer.rel_path == doc.rel_path else ""
        links.append(f'<a class="mini-link{active}" href="{href}">{esc(peer.title)}</a>')
    return "\n".join(links)


