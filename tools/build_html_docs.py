def category_counts(docs: list[Doc]) -> dict[str, int]:
    counts: dict[str, int] = {}
    for doc in docs:
        counts[doc.category] = counts.get(doc.category, 0) + 1
    return dict(sorted(counts.items(), key=lambda item: CATEGORY_RANK.get(item[0], 99)))


def render_count_stats(counts: dict[str, int]) -> str:
    return "\n".join(
        f"<div><dt>{esc(category)}</dt><dd>{count}</dd></div>" for category, count in counts.items()
    )


def render_baseline_route(docs: list[Doc], index_path: Path) -> str:
    selected = [
        "docs/confirmed/project-alignment-summary-zh.md",
        "docs/confirmed/system-constitution-v1.md",
        "docs/confirmed/kb-to-index-projection-contract-zh.md",
        "docs/confirmed/llm-harness-and-agent-integration-alignment-zh.md",
        "docs/confirmed/internal-llm-agent-service-implementation-plan-zh.md",
        "docs/java-service-runbook-zh.md",
        "docs/api-caller-guide-zh.md",
    ]
    docs_by_rel = {doc.rel_path: doc for doc in docs}
    links = []
    for index, rel_path in enumerate(selected, start=1):
        doc = docs_by_rel.get(rel_path)
        if not doc:
            continue
        href = rel_url(index_path, doc.output_path)
        links.append(
            f"""<a class="route-item" href="{href}">
              <span>{index:02d}</span>
              <strong>{esc(doc.title)}</strong>
              <em>{esc(doc.category)}</em>
            </a>"""
        )
    return "\n".join(links)


def render_doc_card(doc: Doc, index_path: Path) -> str:
    href = rel_url(index_path, doc.output_path)
    search_text = " ".join([doc.title, doc.summary, doc.rel_path, doc.category]).lower()
    return f"""<article class="doc-card" data-category="{esc(doc.category)}" data-search="{esc(search_text)}">
      <a href="{href}">
        <div class="card-topline">
          <span class="category-badge">{esc(doc.category)}</span>
          <code>{esc(doc.rel_path)}</code>
        </div>
        <h3>{esc(doc.title)}</h3>
        <p>{esc(doc.summary)}</p>
      </a>
    </article>"""


def write_assets(docs: list[Doc]) -> None:
    (ASSETS / "styles.css").write_text(STYLES, encoding="utf-8")
    (ASSETS / "app.js").write_text(APP_JS, encoding="utf-8")
    docs_payload = [
        {
            "title": doc.title,
            "path": doc.rel_path,
            "category": doc.category,
            "summary": doc.summary,
            "url": rel_url(OUT / "index.html", doc.output_path),
        }
        for doc in docs
    ]
    (ASSETS / "search-index.js").write_text(
        "window.RTS_DOCS = " + json.dumps(docs_payload, ensure_ascii=False, indent=2) + ";\n",
        encoding="utf-8",
    )


def esc(value: Any) -> str:
    return html.escape(str(value), quote=True)


