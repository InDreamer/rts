DOCMETA_RE = re.compile(r"^\s*<!--\s*docmeta\s*\n(.*?)\n-->\s*", re.DOTALL)
MD_LINK_RE = re.compile(r"(?<!!)\[([^\]]+)\]\(([^)]+)\)")


@dataclass
class Doc:
    source_path: Path
    rel_path: str
    output_path: Path
    title: str
    summary: str
    category: str
    meta: dict[str, Any]
    body_markdown: str
    body_html: str = ""
    toc_html: str = ""


def main() -> None:
    docs = load_docs()
    docs_by_rel = {doc.rel_path: doc for doc in docs}

    prepare_output_dir()
    write_assets(docs)

    for doc in docs:
        render_markdown_doc(doc, docs_by_rel)
        write_doc_page(doc, docs, docs_by_rel)

    write_index(docs)
    MARKER.write_text("generated by tools/build_html_docs.py\n", encoding="utf-8")
    print(f"Generated {len(docs)} document pages under {OUT.relative_to(ROOT)}")


def load_docs() -> list[Doc]:
    docs: list[Doc] = []
    for path in ROOT.rglob("*.md"):
        if not is_active_markdown(path):
            continue
        rel_path = path.relative_to(ROOT).as_posix()
        raw = path.read_text(encoding="utf-8")
        meta, body = split_docmeta(raw)
        title = extract_title(body) or path.stem.replace("-", " ").title()
        summary = str(meta.get("summary") or extract_first_paragraph(body) or "")
        docs.append(
            Doc(
                source_path=path,
                rel_path=rel_path,
                output_path=OUT / Path(rel_path).with_suffix(".html"),
                title=title,
                summary=summary,
                category=category_for(rel_path),
                meta=meta,
                body_markdown=body,
            )
        )
    docs.sort(key=sort_key)
    return docs


def is_active_markdown(path: Path) -> bool:
    rel = path.relative_to(ROOT).as_posix()
    if any(rel.startswith(prefix) for prefix in EXCLUDED_PREFIXES):
        return False
    if any(part in EXCLUDED_PARTS for part in path.relative_to(ROOT).parts):
        return False
    return True


def split_docmeta(raw: str) -> tuple[dict[str, Any], str]:
    match = DOCMETA_RE.match(raw)
    if not match:
        return {}, raw.lstrip()
    return parse_docmeta(match.group(1)), raw[match.end() :].lstrip()


def parse_docmeta(block: str) -> dict[str, Any]:
    meta: dict[str, Any] = {}
    current_key: str | None = None
    for line in block.splitlines():
        if not line.strip():
            continue
        key_match = re.match(r"^([A-Za-z_][\w-]*):\s*(.*)$", line)
        if key_match:
            current_key = key_match.group(1)
            value = key_match.group(2).strip()
            meta[current_key] = value if value else []
            continue
        stripped = line.strip()
        if stripped.startswith("- ") and current_key:
            current_value = meta.setdefault(current_key, [])
            if not isinstance(current_value, list):
                current_value = [str(current_value)]
                meta[current_key] = current_value
            current_value.append(stripped[2:].strip())
    return meta


def extract_title(markdown_text: str) -> str:
    for line in markdown_text.splitlines():
        match = re.match(r"^#\s+(.+?)\s*$", line)
        if match:
            return strip_markdown_inline(match.group(1))
    return ""


def extract_first_paragraph(markdown_text: str) -> str:
    lines: list[str] = []
    in_code = False
    for line in markdown_text.splitlines():
        stripped = line.strip()
        if stripped.startswith("```"):
            in_code = not in_code
            continue
        if in_code or not stripped:
            if lines:
                break
            continue
        if stripped.startswith(("#", "-", ">", "|")):
            continue
        lines.append(stripped)
    return strip_markdown_inline(" ".join(lines))


def strip_markdown_inline(text: str) -> str:
    text = re.sub(r"`([^`]+)`", r"\1", text)
    text = re.sub(r"\*\*([^*]+)\*\*", r"\1", text)
    text = re.sub(r"\*([^*]+)\*", r"\1", text)
    text = re.sub(r"\[([^\]]+)\]\([^)]+\)", r"\1", text)
    return text.strip()


def category_for(rel_path: str) -> str:
    if rel_path == "README.md":
        return "Entry"
    if rel_path == "AGENTS.md":
        return "Agent Guide"
    if rel_path == "docs/INDEX.md":
        return "Navigation"
    if rel_path.startswith("docs/confirmed/"):
        return "Confirmed Baseline"
    if rel_path in {
        "docs/java-service-runbook-zh.md",
        "docs/api-caller-guide-zh.md",
        "docs/day1-sample-pack-test-commands.md",
    }:
        return "Operational"
    if rel_path.startswith("docs/reference/"):
        return "Reference"
    if rel_path.startswith("kb/"):
        return "KB Pack"
    return "Other Active"


def sort_key(doc: Doc) -> tuple[int, int, str]:
    ordered = {name: index for index, name in enumerate(ORDERED_DOCS)}
    explicit = ordered.get(doc.rel_path, 10_000)
    return (CATEGORY_RANK.get(doc.category, 99), explicit, doc.rel_path)


def prepare_output_dir() -> None:
    if OUT.exists():
        has_files = any(OUT.iterdir())
        if has_files and not MARKER.exists():
            raise SystemExit(
                f"{OUT} already contains files and is not marked as generated. "
                "Move or remove it before rebuilding."
            )
        shutil.rmtree(OUT)
    ASSETS.mkdir(parents=True, exist_ok=True)


def render_markdown_doc(doc: Doc, docs_by_rel: dict[str, Doc]) -> None:
    rewritten = rewrite_markdown_links(doc.body_markdown, doc, docs_by_rel)
    converter = markdown.Markdown(
        extensions=["extra", "toc", "sane_lists"],
        extension_configs={
            "toc": {
                "permalink": "#",
                "toc_depth": "2-4",
            }
        },
        output_format="html5",
    )
    doc.body_html = converter.convert(rewritten)
    doc.toc_html = converter.toc


def rewrite_markdown_links(markdown_text: str, doc: Doc, docs_by_rel: dict[str, Doc]) -> str:
    def replace(match: re.Match[str]) -> str:
        label, raw_url = match.groups()
        raw_url = raw_url.strip()
        if re.match(r"^(https?:|mailto:|#|javascript:)", raw_url):
            return match.group(0)
        path_part, fragment = split_fragment(raw_url)
        if not path_part:
            return match.group(0)
        target = (doc.source_path.parent / path_part).resolve()
        try:
            target_rel = target.relative_to(ROOT).as_posix()
        except ValueError:
            return match.group(0)
        target_doc = docs_by_rel.get(target_rel)
        if target_doc:
            href = rel_url(doc.output_path, target_doc.output_path) + fragment
        elif target.exists():
            href = rel_url(doc.output_path, target) + fragment
        else:
            title = esc(f"Missing local target: {raw_url}")
            return f'<span class="missing-link" title="{title}">{label}</span>'
        return f"[{label}]({href})"

    return MD_LINK_RE.sub(replace, markdown_text)


def split_fragment(url: str) -> tuple[str, str]:
    if "#" not in url:
        return url, ""
    path_part, fragment = url.split("#", 1)
    return path_part, f"#{fragment}"


def rel_url(from_file: Path, to_path: Path) -> str:
    return os.path.relpath(to_path, from_file.parent).replace(os.sep, "/")


