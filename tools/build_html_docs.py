def render_doc_insights(doc: Doc) -> str:
    role = doc.meta.get("role") or doc.category
    layer = doc.meta.get("layer") or "active"
    intro = display_summary(doc) or first_meaningful_sentences(doc.body_markdown, 2)
    insights = [
        ("文档定位", display_summary(doc) or "说明当前 RTS 活跃文档中的一个主题。"),
        ("核心摘要", intro),
        ("权威层级", f"{zh_category(doc.category)} / role: {role} / layer: {layer}"),
    ]
    cards = []
    for title, text in insights:
        cards.append(f"""<article class="insight-card">
          <h2>{esc(title)}</h2>
          <p>{esc(text)}</p>
        </article>""")
    return "\n".join(cards)


def first_meaningful_sentences(markdown_text: str, count: int) -> str:
    plain = []
    in_code = False
    for line in markdown_text.splitlines():
        stripped = line.strip()
        if stripped.startswith("```"):
            in_code = not in_code
            continue
        if in_code or not stripped or stripped.startswith(("#", "|", "```")):
            continue
        if stripped.startswith("- "):
            continue
        plain.append(strip_markdown_inline(stripped))
        if len(" ".join(plain)) > 220:
            break
    text = " ".join(plain)
    if not text:
        return ""
    pieces = re.split(r"(?<=[。.!?])\s+", text)
    return " ".join(pieces[:count]).strip()


def render_chapter_cards(doc: Doc) -> str:
    sections = extract_heading_sections(doc.body_markdown)
    if not sections:
        return '<article class="chapter-card"><h3>Markdown 原文</h3><p>该文档没有明显章节，请以 Markdown 原文为准。</p></article>'
    cards = []
    for title, excerpt in sections[:12]:
        cards.append(f"""<article class="chapter-card">
          <h3>{esc(title)}</h3>
          <p>{esc(excerpt or "本节说明该主题的细节、边界或操作要求。")}</p>
        </article>""")
    return "\n".join(cards)


def extract_heading_sections(markdown_text: str) -> list[tuple[str, str]]:
    sections: list[tuple[str, list[str]]] = []
    current_title = ""
    current_lines: list[str] = []
    in_code = False
    for line in markdown_text.splitlines():
        stripped = line.strip()
        if stripped.startswith("```"):
            in_code = not in_code
            continue
        if in_code:
            continue
        match = re.match(r"^##\s+(.+?)\s*$", stripped)
        if match:
            if current_title:
                sections.append((strip_markdown_inline(current_title), current_lines))
            current_title = match.group(1)
            current_lines = []
            continue
        if current_title and stripped and not stripped.startswith(("#", "|")):
            current_lines.append(stripped)
    if current_title:
        sections.append((strip_markdown_inline(current_title), current_lines))

    result: list[tuple[str, str]] = []
    for title, lines in sections:
        excerpt_lines = []
        for line in lines:
            if line.startswith("- "):
                excerpt_lines.append(strip_markdown_inline(line[2:]))
            elif not line.startswith(("```", ">")):
                excerpt_lines.append(strip_markdown_inline(line))
            if len(" ".join(excerpt_lines)) > 160:
                break
        result.append((title, " ".join(excerpt_lines)[:220]))
    return result


def render_doc_meta_cards(doc: Doc, docs_by_rel: dict[str, Doc]) -> str:
    blocks = []
    read_when = doc.meta.get("read_when")
    if read_when:
        blocks.append(render_compact_meta_block("适合阅读", read_when, docs_by_rel, doc.output_path))
    skip_when = doc.meta.get("skip_when")
    if skip_when:
        blocks.append(render_compact_meta_block("可跳过", skip_when, docs_by_rel, doc.output_path))
    return "\n".join(blocks)


def render_compact_meta_block(title: str, values: Any, docs_by_rel: dict[str, Doc], current_output: Path) -> str:
    if isinstance(values, str):
        values = [values]
    items = "\n".join(f"<li>{render_meta_value(str(item), docs_by_rel, current_output)}</li>" for item in values[:5])
    return f"""<div class="aside-card">
      <p class="kicker">{esc(title)}</p>
      <ul>{items}</ul>
    </div>"""


def render_related_sources(doc: Doc, docs: list[Doc], docs_by_rel: dict[str, Doc]) -> str:
    source_values = doc.meta.get("source_of_truth") or []
    if isinstance(source_values, str):
        source_values = [source_values]
    links = []
    for value in source_values[:6]:
        clean = str(value).strip().strip("`")
        target = docs_by_rel.get(clean)
        if target:
            href = rel_url(doc.output_path, target.output_path)
            links.append(f'<a href="{href}">{esc(display_title(target))}</a>')
    if not links:
        peers = [item for item in docs if item.category == doc.category and item.rel_path != doc.rel_path][:5]
        for peer in peers:
            links.append(f'<a href="{rel_url(doc.output_path, peer.output_path)}">{esc(display_title(peer))}</a>')
    if not links:
        return ""
    return f"""<div class="aside-card related-card">
      <p class="kicker">关联来源</p>
      {''.join(links)}
    </div>"""


